data class Mesto(
    val nazov: String = "",
    val skratka: String = ""
) {
    fun isMatchingQuery(query: String) : Boolean {
        val matching = listOf(
            nazov
        )
        return matching.any{
            it.contains(query,ignoreCase = true)
        }
    }
}

